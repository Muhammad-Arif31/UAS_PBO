    private void btnCariMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCariMouseClicked
        String keyword = txtCari.getText();
        DefaultTableModel tbl  = (DefaultTableModel) tabelAmplang.getModel();
        tabelAmplang.setRowHeight(40);
        tabelAmplang.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        TableColumnModel model_tabel = tabelAmplang.getColumnModel();
        TableColumn column = model_tabel.getColumn(0);
        TableColumn column_5 = model_tabel.getColumn(5);        
        tbl.setRowCount(0);        
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/dbaplikasiamplang", "root", "");
            // membuat statement dan mengirimkan query ke database
            String sql = "SELECT * FROM tbamplang WHERE nama LIKE '%" + keyword + "%'";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            // mengisi data dari ResultSet ke dalam model
            while(rs.next()) {
                String no = Integer.toString(rs.getInt("no"));
                String namaAmplang = rs.getString("nama");
                String jenis = rs.getString("jenis");
                String ukuran = rs.getString("ukuran");
                String jumlah = rs.getString("jumlah");
                String harga = Double.toString(rs.getDouble("harga"));
                tbl.addRow(new Object []{no, namaAmplang, jenis, ukuran, jumlah, harga});
                tabelAmplang.setModel(tbl);
            }
            conn.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "There is an error : " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCariMouseClicked

