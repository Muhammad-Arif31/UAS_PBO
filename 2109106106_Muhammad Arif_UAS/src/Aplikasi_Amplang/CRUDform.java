    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/dbaplikasiamplang", "root", "");
            String sql = "insert into tbamplang (nama, jenis, ukuran, jumlah, harga) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, txtNama.getText());
            String jenis = "";
            boolean isValidSelection = false;
            if (jRadioButton1.isSelected()) {
                jenis = "Tenggiri";
                isValidSelection = true;
            } else if (jRadioButton2.isSelected()) {
                jenis = "Haruan";
                isValidSelection = true;
            } else if (jRadioButton3.isSelected()) {
                jenis = "Belida";
                isValidSelection = true;
            } else {
                JOptionPane.showMessageDialog(this,
                    "jenis tidak valid, pilih jenis!",
                    "Gagal", JOptionPane.ERROR_MESSAGE);
            }
            stmt.setString(2, jenis);
            stmt.setString(3, cbUkuran.getSelectedItem().toString());
            stmt.setInt(4, Integer.parseInt(txtJumlah.getText()));
            stmt.setDouble(5, Double.parseDouble(txtHarga.getText()));

            int masukan_row = stmt.executeUpdate();

            if(masukan_row > 0){
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!!!");
                txtNama.setText("");
                jRadioButton1.setSelected(false);
                jRadioButton2.setSelected(false);
                jRadioButton3.setSelected(false);
                cbUkuran.setSelectedIndex(-1);
                txtJumlah.setText("");
                txtHarga.setText("");
                table();
            }
            conn.close();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Data gak bisa masuk!!! terjadi error:" + ex.getMessage());
        }
    }//GEN-LAST:event_btnTambahActionPerformed

